public class diffBetweenwhileAndDowhile {
    public static void main(String[] args) {
        int number1 = 10;
        while(number1>10){
            System.out.println("我是渣男");
            number1--;
        };
        int number2 = 10;
        do{
            System.out.println("我是直男");
            number2--;
        }while(number2>10);
    }
}
